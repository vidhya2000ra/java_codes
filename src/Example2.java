public class Example2 {
    public static void main(String args[]){
        int A= 12, B= 144;
        //Relational Operator
        System.out.println(A==B);
        System.out.println(A!=B);
        System.out.println(A>B);
        System.out.println(A<B);
        System.out.println(A>=B);
        System.out.println(A<=B);
    }
}

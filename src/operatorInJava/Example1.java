package operatorInJava;

public class Example1 {
    public static void main(String args[]){
        int a = 20, b= 35;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
    }
}

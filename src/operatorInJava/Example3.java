package operatorInJava;

public class Example3 {
    public static void main(String args[]){
        int a = 65, b=55;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);

    }
}
